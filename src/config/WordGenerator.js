class WordHelper {
  getRandomWord() {
    const keys = Object.keys(words);
    const index = Math.floor(Math.random() * keys.length);
    const word = keys[index].toUpperCase();
    return word;
  }

  existsWord(word) {
    const lowerCaseWord = word.toLowerCase();
    if (words.hasOwnProperty(lowerCaseWord) || Object.values(words).includes(lowerCaseWord)) {
      return true;
    }

    return false;
  }

  isEquals(correctWord, enteredWord) {
    if (correctWord === enteredWord || words[correctWord.toLowerCase()].toUpperCase() === enteredWord) {
      return true;
    }

    return false;
  }
};

export default new WordHelper();